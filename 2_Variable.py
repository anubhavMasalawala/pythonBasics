# Types of Variable in Python


# 1. Numbers: Python accommodates two categories of numerical unit -
    # a. integers, which represent whole numbers, and
    # b. floating-point numbers, which encompass decimal values.

a = 2 # integer
b = 2.3 # floating-point number

print ("This is an integer ")
print(a)
print ("This is an floating-point number")
print(b)


# 2. Strings : Strings can be declared using either single or double quotation marks.

c = "Zilli's"

print("This is a string : " + c)